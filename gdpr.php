    <h2>3.3 Cookie-k (Sütik)</h2>
    <br>
    <h3> 3.3.1 A sütik feladata</h3>
    <ul>
        <li> információkat gyűjtenek a látogatókról és eszközeikről;</li>
        <li> megjegyzik a látogatók egyéni beállításait, amelyek felhasználásra kerül(het)nek pl. online
            tranzakciók igénybevételekor, így nem kell újra begépelni őket;</li>
        <li> megkönnyítik a weboldal használatát;</li>
        <li> minőségi felhasználói élményt biztosítanak.</li>
    </ul>
    A testre szabott kiszolgálás érdekében a felhasználó számítógépén kis adatcsomagot, ún. sütit (cookie)
    helyez el és a későbbi látogatás során olvas vissza. Ha a böngésző visszaküld egy korábban elmentett
    sütit, a sütit kezelő szolgáltatónak lehetősége van összekapcsolni a felhasználó aktuális látogatását a
    korábbiakkal, de kizárólag a saját tartalma tekintetében.
    <br><br>
    <h3> 3.3.2 Feltétlenül szükséges, munkamenet (session) cookie-k</h3>
    Ezen sütik célja, hogy a látogatók maradéktalanul és zökkenőmentesen böngészhessék a szolgáltató weboldalát,
    használhassák annak funkcióit, és az ott elérhető szolgáltatásokat. Az ilyen típusú sütik
    érvényességi ideje a munkamenet (böngészés) befejezéséig tart, a böngésző bezárásával a sütik e
    fajtája automatikusan törlődik a számítógépről, illetve a böngészésre használt más eszközről.
    <br><br>
    <h3>3.3.3 Harmadik fél által elhelyezett cookie-k (analitika)</h3>
    A szolgáltató nem alkalmaz harmadik fél által elhelyezett sütiket.
